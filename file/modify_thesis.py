def replace_para_text(para, new_text):
    """完全替换段落的文本内容"""
    if not para.runs:
        return
    font = para.runs[0].font
    font_name = font.name
    font_size = font.size
    font_bold = font.bold
    for run in para.runs:
        run.text = ''
    para.runs[0].text = new_text
    if font_name:
        para.runs[0].font.name = font_name
    if font_size:
        para.runs[0].font.size = font_size
    if font_bold is not None:
        para.runs[0].font.bold = font_bold


def find_and_replace(doc, pattern, new_text):
    """查找包含pattern的段落并替换为new_text"""
    for para in doc.paragraphs:
        if pattern in para.text and len(para.text.strip()) > 10:
            replace_para_text(para, new_text)
            return True
    return False


def find_and_replace_all(doc, old_str, new_str):
    """在所有段落中替换字符串"""
    for para in doc.paragraphs:
        if old_str in para.text:
            for run in para.runs:
                if old_str in run.text:
                    run.text = run.text.replace(old_str, new_str)


